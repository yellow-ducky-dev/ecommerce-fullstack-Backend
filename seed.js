const seed = async () => {
  try {
    await mongoose.connect(process.env.MONGO_URI, { tls: true, tlsAllowInvalidCertificates: true });
    console.log("MongoDB connected for seeding...");

    // Clear existing
    await Product.deleteMany({});
    await User.deleteMany({});
    console.log("Cleared existing data");

    // Create admin user
    const admin = await User.create({
      name: "Admin User",
      email: "admin@ecommerce.com",
      password: "Admin@1234",
      isAdmin: true,
    });
    console.log("Created admin:", admin.email);

    // Create regular user
    const user = await User.create({
      name: "John Doe",
      email: "john@example.com",
      password: "John@1234",
      isAdmin: false,
    });
    console.log("Created user:", user.email);

    // Seed products with user reference for reviews
    const productsWithUser = products.map((p) => ({
      ...p,
      reviews: (p.reviews || []).map((r) => ({ ...r, user: user._id })),
    }));
    await Product.insertMany(productsWithUser);
    console.log(`Seeded ${productsWithUser.length} products`);

    console.log("\n✅ Database seeded successfully!");
    console.log("Admin: admin@ecommerce.com / Admin@1234");
    console.log("User:  john@example.com / John@1234");
    process.exit(0);
  } catch (error) {
    console.error("Seeding error:", error);
    process.exit(1);
  }
};

seed();
